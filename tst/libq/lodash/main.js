const _ = require('lodash');

_.forEach([1, 2], function(value) {
  console.log(value);
});

function square(n) {
  return n * n;
}

_.map([4, 8], square);
// => [16, 64]

_.reduce([1, 2], function(sum, n) {
  return sum + n;
}, 0);
// => 3

var array = [[0, 1], [2, 3], [4, 5]];
 
_.reduceRight(array, function(flattened, other) {
  return flattened.concat(other);
}, []);
// => [4, 5, 2, 3, 0, 1]
